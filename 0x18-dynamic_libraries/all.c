#include <stdio.h>
/**
 * add - adding two intergers
 * @a: first integer
 * @b: second interger
 * Return: add
 */
int add(int a , int b)
{
	return (a + b);
}
/**
 * sub - subtracting two intergers
 * @a: first integer
 * @b: second interger
 * Return: sub
 */
int sub(int a , int b)
{
	return (a - b);
}
/**
 * div - divide two intergers
 * @a: first integer
 * @b: second interger
 * Return: div
 */
int div(int a , int b)
{
	return (a / b);
}
/**
 * mod - mod two intergers
 * @a: first integer
 * @b: second interger
 * Return: mod
 */
int mod(int a , int b)
{
	return (a % b);
}
/**
 * mul - mul two intergers
 * @a: first integer
 * @b: second interger
 * Return: mul
 */
int mul(int a , int b)
{
	return (a * b);
}
