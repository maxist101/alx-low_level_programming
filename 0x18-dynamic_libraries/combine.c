#include <stdio.h>

/**
 * add - function adds val.
 * @a: First val
 * @b: Second val.
 * Return: Added val.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * div - function divide val.
 * @a: First val
 * @b: Second val
 * Return: Value
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function mod val.
 * @a: First val.
 * @b: Second val
 * Return: Value
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - function multiply val.
 * @a: First val
 * @b: Second val.
 * Return: Value
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - function subtract val.
 * @a: First val.
 * @b: Second val.
 * Return: Subtracted val.
 */
int sub(int a, int b)
{
	return (a - b);
}
